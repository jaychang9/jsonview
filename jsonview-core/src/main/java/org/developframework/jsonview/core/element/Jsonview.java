package org.developframework.jsonview.core.element;

public class Jsonview extends ObjectElement {

	private String id;
	private String extend;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getExtend() {
		return extend;
	}

	public void setExtend(String extend) {
		this.extend = extend;
	}

}
